package Variableanddata;

public class LogicalOpertorsExample {

	public static void main(String[] args) {
		int x=10,y=10;
		System.out.println((x<=y)&& (x==y));
		                  //10<=10=t && 10==10=t
		System.out.println((x<=y)||(x==y));
        //10<=34  || 10==34
		System.out.println((x!=y));

	}

}